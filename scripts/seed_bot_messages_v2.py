def run():
    conn = psycopg2.connect(DATABASE_URL)
    cur = conn.cursor()
    inserted = 0
    for key, text in MESSAGES.items():
        role = f"bot_{key}"
        cur.execute("""
            INSERT INTO prompts (role, version, prompt_text, is_active, updated_by)
            VALUES (%s,
                    COALESCE((SELECT MAX(version) FROM prompts WHERE role = %s), 0) + 1,
                    %s, TRUE, 'seed_bot_messages_v2')
            ON CONFLICT DO NOTHING
        """, (role, role, text))
        # Деактивируем старые версии
        cur.execute("""
            UPDATE prompts SET is_active = FALSE
            WHERE role = %s AND version < (SELECT MAX(version) FROM prompts WHERE role = %s)
        """, (role, role))
        inserted += 1
    conn.commit()
    cur.close()
    conn.close()
    print(f"Сид bot_messages_v2 завершён: {inserted} сообщений.")


if __name__ == "__main__":
    run()
