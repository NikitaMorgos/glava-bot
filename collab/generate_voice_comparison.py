class PDF(FPDF):
    def __init__(self):
        super().__init__()
        self.add_font("Arial", style="",  fname=FONT_REGULAR)
        self.add_font("Arial", style="B", fname=FONT_BOLD)
        self.add_font("Arial", style="I", fname=FONT_ITALIC)
        self.add_font("Arial", style="BI", fname=FONT_BOLDITALIC)
        self.set_margins(25, 20, 25)
        self.set_auto_page_break(auto=True, margin=20)

    def header(self):
        if self.page_no() == 1:
            return
        self.set_font("Arial", "I", 8)
        self.set_text_color(160, 160, 160)
        self.cell(0, 8, "Голоса писателей. Портрет Валентины Каракулиной", align="C")
        self.ln(4)
        self.set_draw_color(220, 220, 220)
        self.line(25, self.get_y(), 185, self.get_y())
        self.ln(4)
        self.set_text_color(0, 0, 0)

    def footer(self):
        self.set_y(-15)
        self.set_font("Arial", "I", 8)
        self.set_text_color(160, 160, 160)
        self.cell(0, 10, str(self.page_no()), align="C")

    def title_page(self):
        self.add_page()
        self.set_y(60)
        self.set_font("Arial", "B", 28)
        self.set_text_color(30, 30, 30)
        self.multi_cell(0, 14, "Три голоса", align="C")
        self.ln(4)
        self.set_font("Arial", "", 16)
        self.set_text_color(80, 80, 80)
        self.multi_cell(0, 10, "Портрет человека в стиле\nЗыгаря · Яхиной · Улицкой", align="C")
        self.ln(16)
        self.set_draw_color(180, 140, 80)
        cx = self.w / 2
        self.line(cx - 30, self.get_y(), cx + 30, self.get_y())
        self.ln(16)
        self.set_font("Arial", "I", 11)
        self.set_text_color(100, 100, 100)
        self.multi_cell(0, 8,
            "Валентина Ивановна Каракулина (1920–…)\n"
            "Эксперимент с авторским голосом для проекта «Глава»",
            align="C")
        self.ln(40)
        self.set_font("Arial", "", 9)
        self.set_text_color(150, 150, 150)
        self.multi_cell(0, 6, "Апрель 2026", align="C")

    def chapter_title(self, num, title, color=(30, 30, 30)):
        self.set_font("Arial", "B", 14)
        self.set_text_color(*color)
        self.ln(4)
        self.multi_cell(0, 9, f"{num}  {title}")
        self.set_draw_color(*color)
        self.line(25, self.get_y() + 1, 185, self.get_y() + 1)
        self.ln(7)
        self.set_text_color(0, 0, 0)

    def section_title(self, title):
        self.set_font("Arial", "B", 11)
        self.set_text_color(60, 60, 60)
        self.ln(4)
        self.multi_cell(0, 7, title)
        self.ln(2)
        self.set_text_color(0, 0, 0)

    def body(self, text):
        self.set_font("Arial", "", 10.5)
        self.set_text_color(30, 30, 30)
        for para in text.strip().split("\n\n"):
            para = para.strip()
            if not para:
                continue
            self.multi_cell(0, 6.5, para)
            self.ln(3.5)

    def expert_card(self, name, label, text):
        self.set_x(25)
        self.set_font("Arial", "B", 11)
        self.set_text_color(40, 40, 40)
        self.multi_cell(160, 7, name, border=0)
        self.set_x(25)
        self.set_font("Arial", "I", 9)
        self.set_text_color(120, 100, 60)
        self.multi_cell(160, 6, label)
        self.ln(1)
        self.set_x(25)
        self.set_font("Arial", "", 10)
        self.set_text_color(50, 50, 50)
        self.multi_cell(160, 6, text)
        self.ln(5)
        self.set_draw_color(220, 210, 190)
        self.line(25, self.get_y(), 185, self.get_y())
        self.ln(5)

    def quote_block(self, text):
        self.set_x(35)
        self.set_font("Arial", "I", 10)
        self.set_text_color(80, 80, 80)
        self.set_draw_color(180, 140, 80)
        x = self.get_x()
        y_start = self.get_y()
        self.multi_cell(145, 6.5, text)
        y_end = self.get_y()
        self.line(30, y_start, 30, y_end)
        self.ln(4)
        self.set_text_color(0, 0, 0)
        self.set_x(25)

    def comparison_row(self, label, col1, col2, col3, shade=False):
        if shade:
            self.set_fill_color(248, 246, 240)
        else:
            self.set_fill_color(255, 255, 255)
        col_w = [45, 45, 45, 45]
        h = 7
        self.set_x(25)
        for i, (txt, bold) in enumerate([(label, True), (col1, False), (col2, False), (col3, False)]):
            self.set_font("Arial", "B" if bold else "", 9)
            self.set_text_color(50, 50, 50)
            self.cell(col_w[i], h, txt, border=1, fill=shade)
        self.ln()


